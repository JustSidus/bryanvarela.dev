/**
 * Build-time injection of the repo's last 3 real commits into the faux PowerShell
 * terminal (see src/components/shell/BootTerminal.jsx). Runs locally during
 * `npm run build`, writes a static JS module — zero runtime fetches, zero tokens.
 *
 * Quality gate: a commit is shown only if it passes ALL of:
 *   - not a merge commit (we use --no-merges, but double-check)
 *   - subject length >= MIN_LEN
 *   - matches conventional-commits prefix (feat|fix|refactor|perf|style|docs|test|chore|build|ci|content)
 *   - not in BLOCKLIST (e.g. "update", "wip", "Update README.md")
 *
 * If we can't collect 3 quality commits, we fall back to a curated synthetic list
 * so the terminal never embarrasses the portfolio.
 */
import { execSync } from "node:child_process";
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dirname, "../src/data/recent-commits.js");

const MIN_LEN = 18;
const CONVENTIONAL = /^(feat|fix|refactor|perf|style|docs|test|chore|build|ci|content)(\([a-z0-9-]+\))?:\s.+/;
const BLOCKLIST = [/^update$/i, /^wip$/i, /^update readme/i, /^fixes?$/i];

const FALLBACK = [
  { hash: "3a4f2b1", msg: "feat(arelify): seo per-tenant via JSON-LD on edge" },
  { hash: "9b2e8d3", msg: "feat(ext): bracketed paste sequences for ai pastes" },
  { hash: "c632b3c", msg: "refactor(api): extract MultiTenantQueryFilter" },
];

function readRealCommits(n = 15) {
  try {
    // Cloudflare Pages does a shallow clone (--depth=1) by default, leaving us
    // with a single commit. Deepen the history silently so the quality gate
    // has enough material to pick 3 commits.
    try {
      execSync(`git fetch --deepen ${n} --quiet`, { stdio: ["pipe", "pipe", "pipe"] });
    } catch {}
    const raw = execSync(
      `git log --no-merges --pretty=%h%x09%s -n ${n}`,
      { encoding: "utf8", stdio: ["pipe", "pipe", "pipe"] }
    ).trim();
    if (!raw) return [];
    return raw.split("\n").map((line) => {
      const tab = line.indexOf("\t");
      return { hash: line.slice(0, tab), msg: line.slice(tab + 1) };
    });
  } catch {
    return [];
  }
}

function isQuality({ msg }) {
  if (!msg || msg.length < MIN_LEN) return false;
  if (BLOCKLIST.some((re) => re.test(msg))) return false;
  return CONVENTIONAL.test(msg);
}

const real = readRealCommits(20);
const passing = real.filter(isQuality).slice(0, 3);
const final = passing.length === 3 ? passing : FALLBACK;
const source = passing.length === 3 ? "git" : "fallback";

const banner = `// AUTO-GENERATED by scripts/generate-recent-commits.mjs at build-time.
// Do not edit by hand — re-run \`npm run build\` (or \`npm run commits\`) to refresh.
// Source: ${source} · ${new Date().toISOString().slice(0, 10)}
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(
  OUT,
  `${banner}export const RECENT_COMMITS = ${JSON.stringify(final, null, 2)};\n`,
);

console.log(
  `✓ Wrote ${OUT} · source=${source} · entries=${final.length}` +
    (source === "fallback" && real.length
      ? ` (real history has ${real.length} commits but <3 passed quality gate)`
      : ""),
);
